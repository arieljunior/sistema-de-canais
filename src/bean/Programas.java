
package bean;

public class Programas {
    
    private String nome;
    private String descricao;

    public Programas(String nome, String descricao) {
        this.nome = nome;
        this.descricao = descricao;
    }

    public String getNome() {
        return nome;
    }

    public String getDescricao() {
        return descricao;
    }


    
    
    
    
}
